package org.beans;
public class Secretaria {
    private String nombre_sec;
    private String username_sec;
    private String password_sec;
    private String direccion_sec;
    private String telefono_sec;
    private String email_sec;
    private String consultorio_nombre_con;

    public String getNombre_sec() {
        return nombre_sec;
    }

    public void setNombre_sec(String nombre_sec) {
        this.nombre_sec = nombre_sec;
    }

    public String getUsername_sec() {
        return username_sec;
    }

    public void setUsername_sec(String username_sec) {
        this.username_sec = username_sec;
    }

    public String getPassword_sec() {
        return password_sec;
    }

    public void setPassword_sec(String password_sec) {
        this.password_sec = password_sec;
    }

    public String getDireccion_sec() {
        return direccion_sec;
    }

    public void setDireccion_sec(String direccion_sec) {
        this.direccion_sec = direccion_sec;
    }

    public String getTelefono_sec() {
        return telefono_sec;
    }

    public void setTelefono_sec(String telefono_sec) {
        this.telefono_sec = telefono_sec;
    }

    public String getEmail_sec() {
        return email_sec;
    }

    public void setEmail_sec(String email_sec) {
        this.email_sec = email_sec;
    }

    public String getConsultorio_nombre_con() {
        return consultorio_nombre_con;
    }

    public void setConsultorio_nombre_con(String consultorio_nombre_con) {
        this.consultorio_nombre_con = consultorio_nombre_con;
    }
    
}
